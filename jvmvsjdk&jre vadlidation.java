final class JDKValidation {
    public static void main(String[] args) {
        
        System.out.println("JDK vs JRE Explanation:");
        System.out.println("----------------------------------");
        System.out.println("JDK (Java Development Kit) = JRE + Compiler + Tools");
        System.out.println(" - Required to compile Java programs using 'javac'");
        System.out.println("JRE (Java Runtime Environment) = JVM + Libraries");
        System.out.println(" - Required to run Java programs but cannot compile them");
        System.out.println("----------------------------------");
        System.out.println("If this program compiled successfully, JDK is installed!");
    }
}
